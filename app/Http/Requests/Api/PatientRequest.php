namespace App\Http\Requests\Api;

class PatientRequest extends JsonRequest
{
    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'name' => 'required|max:150',
            'address' => 'max:250',
            'phones' => 'max:250',
            'birthday' => 'date',
        ];
    }
}
